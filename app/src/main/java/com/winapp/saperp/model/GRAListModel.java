package com.winapp.saperp.model;

public class GRAListModel {

    private String graNumber;
    private String graDate;
    private String supplierName;
    private String supplierCode;
    private String netTotal;
    private String locationCode;

    public String getGraNumber() {
        return graNumber;
    }

    public void setGraNumber(String graNumber) {
        this.graNumber = graNumber;
    }

    public String getGraDate() {
        return graDate;
    }

    public void setGraDate(String graDate) {
        this.graDate = graDate;
    }

    public String getSupplierName() {
        return supplierName;
    }

    public void setSupplierName(String supplierName) {
        this.supplierName = supplierName;
    }

    public String getSupplierCode() {
        return supplierCode;
    }

    public void setSupplierCode(String supplierCode) {
        this.supplierCode = supplierCode;
    }

    public String getNetTotal() {
        return netTotal;
    }

    public void setNetTotal(String netTotal) {
        this.netTotal = netTotal;
    }

    public String getLocationCode() {
        return locationCode;
    }

    public void setLocationCode(String locationCode) {
        this.locationCode = locationCode;
    }
}
